const pasaPalabra = () => {
	let right = 0;
	let wrong = 0;
	let count = 0;
	let newChallenger = askName()
	
	do {
		for(let i=0; i<rosco.length; i++){

			if(rosco[i].status === 0){

			let userAnswer = prompt(rosco[i].question);
			if (userAnswer != null)
			userAnswer = userAnswer.toLowerCase()

			if(userAnswer === rosco[i].answer){
				alert("Correcto!");
				rosco[i].status = 1;
				right++;
				count++;
			}
			else if(userAnswer === 'pasapalabra' || userAnswer === '' || userAnswer === null){
				alert('Pasamos palabra');
			}
			else if(userAnswer === 'end'){
				alert ("Has acertado " + right + " palabras y has cometido " + wrong + " errores.");
				return
			}
			else {
				alert(`WRONG WORD! The right answer was: ${rosco[i].answer}`);
				rosco[i].status = 2;
				wrong++;
				count++;
			}
		}}
	} while (count < rosco.length);
	alert("Has acertado " + right + " palabras y has cometido " + wrong + " errores.")
    scores.push({name: newChallenger, right: right, wrong: wrong})
	finalRanking();
	}

